export function buildIntroductionPrompt(
  resumeText: string,
  jdText: string,
  starMatchSummary: string,
  companyText?: string
): string {
  const companySection = companyText
    ? `\n## 目标公司信息\n${companyText.slice(0, 800)}\n（如果相关，可以自然提到为什么对这家公司感兴趣，但不要硬夸）`
    : "";

  return `请根据以下信息，生成三版自我介绍。要求：用求职者第一人称"我"，专业但不模板化，不念简历，讲能力和价值。

## 我的简历
${resumeText.slice(0, 2000)}

## 目标岗位 JD
${jdText.slice(0, 1500)}
${companySection}

## STAR 匹配结果
${starMatchSummary.slice(0, 1200)}
（matched 是能匹配的能力——用具体事例展开；gaps 是欠缺的——诚实说明，展示可迁移的关联能力）

## 输出要求
- 三个版本各有侧重，内容不要重复
- 不要出现"本人具备""综上所述""在XX领域有丰富经验"这类AI套话
- 匹配的能力用具体事例，不匹配的展示可迁移能力
- 语言专业自信，但像真人说的话，不像书面报告`;
}
