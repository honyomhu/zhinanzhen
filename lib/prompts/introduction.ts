我觉得我跟这个岗位比较契合的一点是：我既懂技术——知道怎么查数据库、怎么排查数据异常，又习惯站在客户视角去理解问题。很多纯客服岗的人缺技术判断力，纯技术岗的人又不愿意跟客户打交道。我刚好在中间这个位置。

