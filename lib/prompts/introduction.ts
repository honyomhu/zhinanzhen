export function buildIntroductionPrompt(
  resumeText: string,
  jdText: string,
  starMatchSummary: string,
  companyText?: string
): string {
  return `请根据以下信息，生成一段面试自我介绍。质量参照：不是复述简历内容，而是讲清楚"我为什么天然适合这个岗位"。

## 求职者简历
${resumeText.slice(0, 2500)}

## 目标岗位 JD
${jdText.slice(0, 1200)}
${companyText ? `\n## 目标公司\n${companyText.slice(0, 600)}` : ""}

## STAR 匹配结果
${starMatchSummary.slice(0, 1500)}
（matched 是可以展开的能力强项，从中选 3-4 个最有说服力的写入自我介绍）

## 关键要求
- 用简历中的真实姓名、公司/学校名称、具体数字
- 每条能力都必须有简历中可查证的具体行为作为证据，不能贴标签
- 不要"本人具备""综上所述""在XX领域有丰富经验"这类AI套话
- 不要硬加短板，除非真的存在明显缺口`;
}
